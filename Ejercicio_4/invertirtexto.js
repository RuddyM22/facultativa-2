// Este código toma una cadena de texto y la devuelve en orden inverso.

//Función que divide el texto, lo invierte y lo vuelve a unir.
let invertirCadena = texto => (texto.split('').reverse().join(''));

//Se llama la función y se le envía el texto a invertir como parámetro.
console.log(invertirCadena('otxeT')); // Resultado esperado: Texto.