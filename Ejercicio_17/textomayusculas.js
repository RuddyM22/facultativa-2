// Este código devuelve una cadena de texto dada en mayúsculas.

// Función que convierte un texto recibido como parámetro en mayúsculas y lo retorna.
let textoEnMayusculas = cadena => cadena.toUpperCase();

// Probando la función.
console.log(textoEnMayusculas('palabra')); // PALABRA.
console.log(textoEnMayusculas('minúsculas')); // MINÚSCULAS.