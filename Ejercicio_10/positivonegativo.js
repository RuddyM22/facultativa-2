// El código devuelve 'true' si el valor es positivo, 'false' en caso contrario.

// Función que evalúa si un número recibido como parámetro es positivo.
let esPositivo = num => num >= 0;

// Pruebas de la función.
console.log(esPositivo(9)); // true.
console.log(esPositivo(-45645)); // false.
console.log(esPositivo(0)); // true.