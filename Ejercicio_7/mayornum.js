// Este código devuelve el mayor de dos números dados.

// Función que encuentra y devuelve el mayor de dos números recibidos como parámetros.
let numeromayor = (num1, num2) => Math.max(num1, num2);

// Llamando la función y enviándole números de prueba como parámetro.
console.log(numeromayor(67, 76)); // 76.
console.log(numeromayor(19, 10)); // 19.