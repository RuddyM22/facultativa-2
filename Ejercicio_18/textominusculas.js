// Este código devuelve una cadena de texto dada en minúsculas.

// Función que convierte un texto recibido como parámetro en minúsculas y lo retorna.
let textoEnMinusculas = cadena => cadena.toLowerCase();

// Probando la función
console.log(textoEnMinusculas('PALABRA')); // palabra.
console.log(textoEnMinusculas('MINÚSCULAS')); // minúsculas.