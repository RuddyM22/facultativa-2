/* Este código toma tres números y devuelve el resultado de la suma del primer y segundo número, multiplcado por el tercero.
(num1 + num2) * num3 */

// Función que suma los dos primeros números y el resultado lo multiplica por el tercero.
let sumarYMultiplicar = (num1, num2, num3) => (num1 + num2) * num3;

// Probando la función.
console.log(sumarYMultiplicar(3, 2, 5)); // 25.
console.log(sumarYMultiplicar(6, 3, 4)); // 36.